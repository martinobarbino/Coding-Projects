/**
	This program prints out a star pattern using asterisk characters.
*/

public class StarPattern
{
	public static void main(String[] args)
	{
		System.out.println("   *");
		System.out.println("  ***");
		System.out.println(" *****");
		System.out.println("*******");
		System.out.println(" *****");
		System.out.println("  ***");
		System.out.println("   *");
	}
}